# Create a list called fruits with at least three fruit names.
# Print the list.
# Print the first and last items using their index.


# Creating a list called fruits with at least three fruit names
fruits = ["Apple", "Banana", "Cherry"]
# Printing the list
print(fruits)
# Printing the first and last items using their index
print("First fruit:", fruits[0])  # First item (index 0)
print("Last fruit:", fruits[-1])  # Last item (index -1)
