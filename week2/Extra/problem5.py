# Create a list of animals and use a for loop to print each animal’s name.
# Add a message after the loop to thank the user.

# Creating a list of animals
animals = ["Dog", "Cat", "Elephant", "Giraffe", "Lion"]
# Using a for loop to print each animal's name
for animal in animals:
    print(animal)
# Adding a message after the loop to thank the user
print("Thank you for viewing the list of animals!")


    