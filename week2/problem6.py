# ------------------------------
# Create two boolean variables: is_sunny = True, is_warm = False.
# Use logical operators to print:
#   - is_sunny AND is_warm
#   - is_sunny OR is_warm
#   - NOT is_sunny

#Creating boolean variables
is_sunny = True
is_warm = False

#Using logical operators to print the results
print("is_sunny AND is_warm:", is_sunny and is_warm)
print("is_sunny OR is_warm:", is_sunny or is_warm)
print("NOT is_sunny:", not is_sunny)
